// [AGENT] 날씨 지역 상수 — GEO_ORDER(시도 순서), CITY_TO_PROVINCE(광역시→시도 매핑)
// 사용처: cesiumUtils.js, useWeatherData.ts, WeatherPanel.jsx
// Purpose: 지역 관련 상수 — 10개 시도 순서(GEO_ORDER) 및 도시-시도 매핑(CITY_TO_PROVINCE)

/**
 * ─────────────────────────────────────────────────────────────────
 *  이 파일의 역할 (지역 상수 정의)
 * ─────────────────────────────────────────────────────────────────
 *  기상청 날씨 3D 지도에서 사용하는 지역 관련 상수 2개를 정의.
 *
 *  1. GEO_ORDER: Cesium 3D 지도에 시도 polygon을 렌더링할 순서 목록
 *  2. CITY_TO_PROVINCE: 특별·광역시 → 소속 시도 매핑 테이블
 *
 *  사용처:
 *    - cesiumUtils.js:      GEO_ORDER 순서대로 polygon 생성
 *    - useWeatherData.ts:   CITY_TO_PROVINCE로 도시를 시도로 정규화
 *    - WeatherPanel.jsx:    GEO_ORDER 순서로 날씨 목록 정렬
 *
 *  왜 별도 파일로 분리했나?
 *    - 상수(변하지 않는 값)는 컴포넌트/훅 파일과 분리해 관리.
 *    - 새로운 지역 추가, 순서 변경 등 수정 시 이 파일 하나만 변경.
 *    - jQuery 비유: var Constants = {GEO_ORDER: [...], CITY_TO_PROVINCE: {...}}; 처럼
 *      전역 상수를 별도 파일에 선언하는 패턴.
 * ─────────────────────────────────────────────────────────────────
 */

// ─────────────────────────────────────────────────────────────────
//  1. 시도 렌더링 순서
// ─────────────────────────────────────────────────────────────────

/**
 * GEO_ORDER: Cesium 지도에서 시도 polygon을 렌더링하는 순서 배열.
 *
 * 포함 범위:
 *   기상청 단기예보 API에서 제공하는 10개 광역 행정구역(시도).
 *   특별시(서울), 광역시(부산, 대구, 인천, 광주, 대전, 울산)는 별도 polygon이 없고
 *   인접 도(경기도, 전라남도 등)에 포함되거나 CITY_TO_PROVINCE로 매핑됨.
 *
 * 이 순서가 중요한 이유:
 *   1. Cesium polygon Z-order(위에 그려지는 순서):
 *      배열 뒤쪽 항목이 나중에 그려져 앞쪽 항목 위에 올라올 수 있음.
 *      지리적으로 인접한 지역이 겹치지 않도록 순서 고려.
 *   2. WeatherPanel.jsx의 날씨 목록 정렬:
 *      이 순서대로 시도 목록을 화면에 나열.
 *      북쪽(서울)에서 남쪽(제주)으로 내려가는 직관적인 순서.
 *   3. useWeatherData.ts의 데이터 매핑:
 *      API 응답의 지역명이 이 배열에 포함되어 있는지 확인할 때 사용.
 *
 * 순서 설명 (대략 북→남, 동→서 순):
 *   서울, 경기 → 강원 → 충청 → 전라 → 경상 → 제주
 *
 * 추후 지역 추가 방법:
 *   이 배열에 시도명 문자열을 추가하고,
 *   해당 시도의 GeoJSON 좌표를 cesiumUtils.js에 추가.
 *   백엔드 WeatherScheduler도 해당 지역 좌표(nx, ny)를 추가해야 함.
 */
export const GEO_ORDER = [
  "서울특별시",
  "경기도",
  "강원도",
  "충청북도",
  "충청남도",
  "전라북도",
  "경상북도",
  "전라남도",
  "경상남도",
  "제주특별자치도",
];

// ─────────────────────────────────────────────────────────────────
//  2. 도시 → 시도 매핑 테이블
// ─────────────────────────────────────────────────────────────────

/**
 * CITY_TO_PROVINCE: 특별·광역시 이름 → 소속 시도 이름 매핑 테이블.
 *
 * 왜 이 매핑이 필요한가?
 *   기상청 API는 "광주", "대구"처럼 광역시 단독 이름으로 데이터를 반환함.
 *   하지만 Cesium 지도의 polygon은 "전라남도", "경상북도" 단위로 그려져 있음.
 *   → "광주" 데이터를 "전라남도" polygon에 색칠하려면 매핑 변환이 필요.
 *
 * 처리 흐름:
 *   API 응답 { region: "광주", tmp: 22 }
 *     → CITY_TO_PROVINCE["광주"] = "전라남도"
 *     → "전라남도" polygon에 온도 22°C 색상 적용
 *
 * 왜 광역시들이 시도 내에 묶이는가?
 *   기상청 단기예보 수집 좌표(nx, ny)를 각 시도 대표 지점 1곳씩만 수집하는 구조.
 *   광주광역시의 예보 데이터를 전라남도 polygon 색상에 사용하는 방식으로 대응.
 *   (추후 광역시별 별도 polygon을 만들면 이 매핑이 필요 없어짐)
 *
 * 포함 도시 설명:
 *   - 광주: 광주광역시 → 전라남도 권역
 *   - 대구: 대구광역시 → 경상북도 권역
 *   - 대전: 대전광역시 → 충청남도 권역
 *   - 울산: 울산광역시 → 경상남도 권역
 *   - 부산: 부산광역시 → 경상남도 권역
 *   - 인천: 인천광역시 → 경기도 권역
 *   - 세종: 세종특별자치시 → 충청남도 권역 (충청남도와 인접)
 *
 * 미포함 도시:
 *   - 서울: GEO_ORDER에 "서울특별시"로 직접 포함되어 있어 매핑 불필요.
 *
 * Record<string, string> 타입:
 *   key: string, value: string 인 객체 타입의 축약형.
 *   TypeScript의 제네릭 유틸리티 타입.
 *   jQuery 비유: var CITY_TO_PROVINCE = {"광주":"전라남도", ...}; 와 동일하지만
 *   TypeScript가 키/값 타입을 보장해줌.
 */
export const CITY_TO_PROVINCE: Record<string, string> = {
  "광주": "전라남도",
  "대구": "경상북도",
  "대전": "충청남도",
  "울산": "경상남도",
  "부산": "경상남도",
  "인천": "경기도",
  "세종": "충청남도",
};
